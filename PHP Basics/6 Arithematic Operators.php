<?php
// Arithmetic Operators in PHP
// + : Addition
// - : Subtraction
// * : Multiplication
// / : Division
// % : Modulus (Remainder)
// ** : Exponentiation (Power)

$a = 10;
$b = 3;

$c = $a + $b; // Addition
$d = $a - $b; // Subtraction
$e = $a * $b; // Multiplication
$f = $a / $b; // Division
$g = $a % $b; // Modulus
$h = $a ** $b; // Exponentiation

echo $h;